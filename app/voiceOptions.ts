  // Add a labels field to each voice option and update the grouping logic
  export const voiceLabels = {
    RECOMMENDED: {
      value: "recommended",
      label: "Recommended",
      icon: "/images/ai/logos/star.svg",
    },
    MALE: {
      value: "male",
      label: "Male Voices",
    },
    FEMALE: {
      value: "female",
      label: "Female Voices",
    },
  };
  
  // Helper function to get grouped voices
  export const getGroupedVoices = (promptType) => {
    const recommendedIds = voiceGroupings[promptType]?.recommended || [];
  
    const groups = [
      {
        label: voiceLabels.RECOMMENDED.label,
        icon: voiceLabels.RECOMMENDED.icon,
        options: voiceOptions.filter((voice) =>
          recommendedIds.includes(voice.value)
        ),
      },
      {
        label: voiceLabels.FEMALE.label,
        options: voiceOptions.filter(
          (voice) =>
            voice.labels.includes("female") &&
            !recommendedIds.includes(voice.value)
        ),
      },
      {
        label: voiceLabels.MALE.label,
        options: voiceOptions.filter(
          (voice) =>
            voice.labels.includes("male") && !recommendedIds.includes(voice.value)
        ),
      },
    ];
  
    return groups.filter((group) => group.options.length > 0);
  };
  